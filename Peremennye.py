# Объявляем переменные
completed_tasks = 12
spent_hours = 1.5
course_name = 'Python'

# Вычисляем время на одно задание
average_time_per_task = spent_hours / completed_tasks

# Выводим результат на экран
print(f"Курс: {course_name}, всего задач: {completed_tasks}, затрачено часов: {spent_hours}, среднее время выполнения {average_time_per_task} часа.")
